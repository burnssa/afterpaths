def test_claude_manual_content_survives_round_trip(tmp_path, gotchas_rules):
    exporter = ClaudeExporter()
    exporter.export(gotchas_rules, tmp_path)

    path = tmp_path / ".claude" / "rules" / "gotchas.md"
    original = path.read_text()
    manual_body = "These are my SCOTUS-probing methodology notes.\n- bullet 1\n- bullet 2"
    edited = original.replace(
        MANUAL_HELPER_LINE,
        manual_body,
    )
    path.write_text(edited)

    # Re-export with different rules
    new_rules = {"gotchas": [_rule("gotchas", "New", "different content")]}
    exporter.export(new_rules, tmp_path)

    final = path.read_text()
    assert manual_body in final
    assert "- **New**: different content" in final
    # The old rule must be gone
    assert "- **G**: watch for Z" not in final


