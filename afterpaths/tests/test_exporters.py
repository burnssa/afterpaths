def test_claude_load_existing_ignores_manual_section(tmp_path):
    rules_dir = tmp_path / ".claude" / "rules"
    rules_dir.mkdir(parents=True)
    path = rules_dir / "gotchas.md"
    path.write_text(
        "# Gotchas\n\n"
        f"{MANUAL_START}\n"
        "- **Manual rule**: should NOT be re-ingested\n  _Source: manualrule_\n"
        f"{MANUAL_END}\n\n"
        f"{AUTO_START}\n"
        "## Rules\n\n"
        "- **Auto rule**: should be re-ingested\n  _Source: abc12345_\n\n"
        "---\n_Generated by afterpaths_\n"
        f"{AUTO_END}\n"
    )

    exporter = ClaudeExporter()
