# -- run_extract_rules integration (force flag) ------------------------------


def test_force_bypasses_unmanaged_check(tmp_path, monkeypatch, all_category_rules):
    """--force should skip the unmanaged check and overwrite."""
    from afterpaths import rules as rules_mod

    # Seed an unmanaged legacy file
    rules_dir = tmp_path / ".claude" / "rules"
    rules_dir.mkdir(parents=True)
    path = rules_dir / "gotchas.md"
    path.write_text(
        "# Gotchas\n\nUnmanaged prose that would normally block the run.\n"
    )

    # Stub out summary loading and extraction so the function runs end-to-end
    # without touching the filesystem's summary store or the LLM.
    monkeypatch.setattr(
        rules_mod,
        "load_recent_summaries",
        lambda days: [("sess1", "content", None)],
    )
    monkeypatch.setattr(
        rules_mod,
        "extract_rules_from_summaries",
        lambda summaries: all_category_rules,
    )
    monkeypatch.setattr(
        rules_mod, "merge_rules", lambda existing, new: new
    )
    monkeypatch.setattr(rules_mod, "update_rules_metadata", lambda ids: None)
    monkeypatch.setattr(rules_mod, "get_rules_metadata", lambda: {})

    # Without --force: aborts.
    result = rules_mod.run_extract_rules(
        days=7, target="claude", project_root=tmp_path, force=False
    )
    assert result.status == "unmanaged_content"
    assert path in result.unmanaged_files
    # File still has its original unmanaged prose
    assert "Unmanaged prose" in path.read_text()

    # With --force: succeeds.
    result = rules_mod.run_extract_rules(
        days=7, target="claude", project_root=tmp_path, force=True
    )
    assert result.status == "success"
    final = path.read_text()
    assert "Unmanaged prose" not in final
    assert MANUAL_START in final
    assert AUTO_START in final
