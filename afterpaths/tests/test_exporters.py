# -- base helpers ------------------------------------------------------------


def test_extract_manual_section_returns_none_when_absent():
    assert extract_manual_section("# Title\n\nnothing here") is None


def test_extract_manual_section_returns_inner_content():
    content = f"# t\n\n{MANUAL_START}\nhello\nworld\n{MANUAL_END}\n"
    assert extract_manual_section(content) == "hello\nworld"


def test_extract_auto_section_returns_inner_content():
    content = f"{AUTO_START}\nbody\n{AUTO_END}"
    assert extract_auto_section(content) == "body"


def test_has_unmanaged_content_true_for_extra_prose():
    content = (
        "# Gotchas\n\n"
        "This is a hand-written note that is not in any markers.\n\n"
        f"{AUTO_START}\n"
        "## Rules\n\n"
        "- **X**: y\n  _Source: abc_\n\n"
        "---\n_Generated by afterpaths_\n"
        f"{AUTO_END}\n"
    )
    assert has_unmanaged_content(content) is True


def test_has_unmanaged_content_false_for_legacy_boilerplate_only():
    content = (
        "# Gotchas\n\n"
        "> Auto-generated by `afterpaths distill` | Updated: 2026-04-21 10:00\n"
        "> Sources: 1 session(s)\n\n"
        "## Rules\n\n"
        "- **X**: y\n  _Source: abc_\n\n"
        "---\n_Generated by afterpaths_\n"
    )
    assert has_unmanaged_content(content) is False


def test_has_unmanaged_content_false_for_markers_with_empty_manual():
    content = (
        "# Gotchas\n\n"
        f"{MANUAL_START}\n{MANUAL_HELPER_LINE}\n{MANUAL_END}\n\n"
        f"{AUTO_START}\n"
        "> Auto-generated by `afterpaths distill` | Updated: 2026-04-21 10:00\n"
        "> Sources: 1 session(s)\n\n"
        "## Rules\n\n"
        "- **X**: y\n  _Source: abc_\n\n"
        "---\n_Generated by afterpaths_\n"
        f"{AUTO_END}\n"
    )
    assert has_unmanaged_content(content) is False


def test_has_unmanaged_content_ignores_manual_body():
    content = (
        "# Gotchas\n\n"
        f"{MANUAL_START}\n"
        "This is a totally free-form paragraph the user wrote. "
        "It contains all kinds of non-boilerplate text, bullet points, random prose.\n"
        "- not a rule line\n"
        f"{MANUAL_END}\n\n"
        f"{AUTO_START}\n## Rules\n\n---\n_Generated by afterpaths_\n{AUTO_END}\n"
    )
    assert has_unmanaged_content(content) is False


def test_has_unmanaged_content_true_for_content_after_auto_end():
    content = (
        "# Gotchas\n\n"
        f"{AUTO_START}\n## Rules\n\n---\n_Generated by afterpaths_\n{AUTO_END}\n"
        "Stray content below the auto section.\n"
    )
    assert has_unmanaged_content(content) is True


